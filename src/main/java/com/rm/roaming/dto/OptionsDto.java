package com.rm.roaming.dto;

public class OptionsDto {

	private String options;

	public OptionsDto(String options) {
		super();
		this.options = options;
	}

	public OptionsDto() {
		super();
	}

	public String getOptions() {
		return options;
	}

	public void setOptions(String options) {
		this.options = options;
	}

	
		
}
